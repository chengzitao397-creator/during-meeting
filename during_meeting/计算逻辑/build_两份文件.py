# -*- coding: utf-8 -*-
"""
生成两份文件：
1. 会议名单：会议名称、意义、大概召开时间
2. 会议历届时间：按图格式，每会每届/每年一行，会议名+逗号对齐+开始 至 结束
"""
import csv
from pathlib import Path

def main():
    # 项目根目录；会议名称类输出到「会议名称」，历届时间类输出到「时间」
    base = Path(__file__).resolve().parent.parent
    dir_name = base / "会议名称"
    dir_time = base / "时间"

    # ---------- 1. 会议名单（意义 + 大概召开时间）----------
    # 从会议大全提取并整理为「类型」维度，含意义与大概时间
    list_rows = [
        ("会议名称", "意义", "大概召开时间"),
        ("全国人大年度会议", "宏观政策、财政预算、立法；政策预期与预算方向", "每年3月（通常3月5日开幕）"),
        ("全国政协年度会议", "政策议题、协商建言、民生；舆论与主题催化", "每年3月，与人大同期"),
        ("中共中央全会（含三中全会）", "改革与治理、五年规划建议、党建；三中全会常被解读为改革信号", "每届多次，三中全会多在下半年"),
        ("中国共产党全国代表大会", "顶层战略、人事与治理、中长期规划；大级别预期切换", "每5年一次，多在10-11月"),
        ("中央经济工作会议", "年度宏观经济、财政货币政策、地产与稳增长定调", "每年11-12月"),
        ("全国金融工作会议", "金融改革与监管顶层部署", "不定期，历届约隔5年（1997、2002、2007、2012、2017、2023）"),
        ("中国进出口商品交易会（广交会）", "外贸、出口订单、制造业；订单与出口链预期", "每年春交会约4月中旬、秋交会约10月中旬"),
        ("中国国际进口博览会（进博会）", "对外开放、进口与消费升级；消费与医药器械等主题", "每年11月5-10日，上海"),
        ("三中全会", "每届中央委员会第三次全会，多聚焦改革与重大决定", "十一届1978-12至二十届2024-07，具体见历届时间表"),
        ("两会", "全国人大与政协年度会议，政策与立法预期", "每年3月（人大一般3月5日开幕）"),
        ("中央经济工作会议（各年）", "当年经济工作总基调与政策方向", "每年12月前后，本表有2012-2025各届具体日期"),
        ("证监会系统工作会议", "资本市场监管年度部署", "每年1-2月"),
        ("全国科技创新大会", "科技创新与人才战略部署", "不定期（2006、2016、2022、2023等）"),
        ("中央城镇化工作会议", "城镇化与城乡发展政策", "不定期（2013、2023等）"),
        ("一带一路国际合作高峰论坛", "一带一路合作与开放表态", "2017、2019、2023等，日期不固定"),
        ("陆家嘴论坛", "金融改革、资本市场制度与开放；监管高层表态", "每年6月"),
        ("中国发展高层论坛", "宏观政策沟通、外资预期", "每年3月，多在两会后"),
        ("中关村论坛", "科创政策、关键技术、产业生态", "每年3-5月，北京"),
        ("世界互联网大会（乌镇）", "数字经济、网络安全与治理", "每年10-11月，乌镇"),
        ("世界人工智能大会", "人工智能、算力与应用生态", "每年7-8月，上海"),
        ("中国国际高新技术成果交易会（高交会）", "高新技术、成果转化与产业招商", "每年11月，深圳"),
        ("中国国际服务贸易交易会（服贸会）", "服务贸易、数字经济、文旅与金融", "每年8-9月，北京"),
        ("中国—东盟博览会", "区域合作、外贸与跨境物流", "每年9月，南宁"),
        ("中国国际消费品博览会（消博会）", "消费、免税与旅游会展", "每年4月，海南"),
    ]

    with open(dir_name / "会议名单.csv", "w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerows(list_rows)

    # ---------- 2. 会议历届时间（每会每届一行，图格式：名称+逗号对齐+开始 至 结束）----------
    # 每条：(会议名称, 开始日期, 结束日期)，日期格式 YYYY-MM-DD
    LIJIE_LINES = []

    # 从会议大全中已有「时间」的行解析出 (名称, 开始, 结束)
    with open(dir_name / "会议大全.csv", "r", encoding="utf-8") as f:
        reader = csv.reader(f)
        header = next(reader)
        name_idx = header.index("event_name_cn")
        time_idx = header.index("时间")
        for row in reader:
            if len(row) <= max(name_idx, time_idx):
                continue
            name = row[name_idx].strip()
            time_str = (row[time_idx] or "").strip()
            if not time_str or "至" not in time_str or "；" in time_str or "本表" in time_str or "通常" in time_str or "泛指" in time_str:
                continue
            # 解析 "2002-11-08 至 2002-11-14" 或 "2002-11-08 至 2002-11-14"
            parts = time_str.split("至")
            if len(parts) != 2:
                continue
            start = parts[0].strip().strip("至").strip()
            end = parts[1].strip()
            if len(start) >= 10 and len(end) >= 10:
                LIJIE_LINES.append((name, start[:10], end[:10]))

    # 不定期会议日期校正（按公开报道）：便于查会议窗口做股票数据
    CORRECT_DATES = {
        "全国金融工作会议（2002）": ("2002-02-05", "2002-02-07"),  # 报道为2月5-7日
        "全国金融工作会议（2017）": ("2017-07-14", "2017-07-15"),  # 报道为7月14-15日
    }
    for i, (n, s, e) in enumerate(LIJIE_LINES):
        if n in CORRECT_DATES:
            s2, e2 = CORRECT_DATES[n]
            LIJIE_LINES[i] = (n, s2, e2)
    # 补全：全国科技创新大会（2012）2012-07-06至07
    if not any(x[0] == "全国科技创新大会（2012）" for x in LIJIE_LINES):
        LIJIE_LINES.append(("全国科技创新大会（2012）", "2012-07-06", "2012-07-07"))

    # 三中全会：逐届展开
    SANZHONG = [
        ("十一届三中全会", "1978-12-18", "1978-12-22"),
        ("十二届三中全会", "1984-10-20", "1984-10-20"),
        ("十三届三中全会", "1988-09-26", "1988-09-30"),
        ("十四届三中全会", "1993-11-11", "1993-11-14"),
        ("十五届三中全会", "1998-10-12", "1998-10-14"),
        ("十六届三中全会", "2003-10-11", "2003-10-14"),
        ("十七届三中全会", "2008-10-09", "2008-10-12"),
        ("十八届三中全会", "2013-11-09", "2013-11-12"),
        ("十九届三中全会", "2018-02-26", "2018-02-28"),
        ("二十届三中全会", "2024-07-15", "2024-07-18"),
    ]
    for name, s, e in SANZHONG:
        LIJIE_LINES.append((name, s, e))

    # 进博会：逐届
    CIIE = [
        ("第一届中国国际进口博览会", "2018-11-05", "2018-11-10"),
        ("第二届中国国际进口博览会", "2019-11-05", "2019-11-10"),
        ("第三届中国国际进口博览会", "2020-11-05", "2020-11-10"),
        ("第四届中国国际进口博览会", "2021-11-05", "2021-11-10"),
        ("第五届中国国际进口博览会", "2022-11-05", "2022-11-10"),
        ("第六届中国国际进口博览会", "2023-11-05", "2023-11-10"),
        ("第七届中国国际进口博览会", "2024-11-05", "2024-11-10"),
    ]
    for name, s, e in CIIE:
        LIJIE_LINES.append((name, s, e))

    # 两会：近年逐年（3月5日人大开幕，约10天）
    for y in range(2013, 2026):
        if y == 2020:
            LIJIE_LINES.append((f"{y}年两会", "2020-05-22", "2020-05-28"))  # 疫情推迟
        else:
            LIJIE_LINES.append((f"{y}年两会", f"{y}-03-05", f"{y}-03-15"))

    # 只取 1980 年及以后
    CUTOFF = "1980-01-01"
    LIJIE_LINES = [(name, s, e) for name, s, e in LIJIE_LINES if s >= CUTOFF]

    # 会议类型（用于分组）：同类型排在一起，组内按时间从老到新
    def meeting_type(name):
        if "届三中全会" in name:
            return "三中全会"
        if "中国共产党" in name and "全国代表大会" in name:
            return "中国共产党全国代表大会"
        if "中央经济工作会议" in name:
            return "中央经济工作会议"
        if "全国金融工作会议" in name:
            return "全国金融工作会议"
        if "证监会系统工作会议" in name:
            return "证监会系统工作会议"
        if "全国科技创新大会" in name:
            return "全国科技创新大会"
        if "中央城镇化工作会议" in name:
            return "中央城镇化工作会议"
        if "国务院常务会议" in name:
            return "国务院常务会议"
        if "发改委会议" in name:
            return "发改委会议"
        if "工信部会议" in name:
            return "工信部会议"
        if "生态环境部会议" in name:
            return "生态环境部会议"
        if "一带一路" in name:
            return "一带一路国际合作高峰论坛"
        if "年两会" in name:
            return "两会"
        if "中国国际进口博览会" in name:
            return "中国国际进口博览会"
        return name

    # 排序：先按会议类型，再按开始日期从老到新
    LIJIE_LINES.sort(key=lambda x: (meeting_type(x[0]), x[1], x[0]))

    # 按图格式写出：会议名称 + 逗号对齐（使日期列对齐）+ 开始 至 结束
    # 目标：日期从约第 40 字符开始
    TARGET_LEN = 42
    lines_out = []
    for name, start, end in LIJIE_LINES:
        date_str = f"{start} 至 {end}"
        n_commas = max(0, TARGET_LEN - len(name))
        line = name + "," * n_commas + date_str
        lines_out.append(line)

    with open(dir_time / "会议历届时间.txt", "w", encoding="utf-8") as f:
        f.write("\n".join(lines_out))

    # 同时写一份 CSV（两列：会议名称, 时间）
    with open(dir_time / "会议历届时间.csv", "w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["会议名称", "时间"])
        for name, start, end in LIJIE_LINES:
            w.writerow([name, f"{start} 至 {end}"])

    print("已生成：")
    print("  1. 会议名单.csv — 会议名称、意义、大概召开时间")
    print("  2. 会议历届时间.txt — 按图格式每会每届一行（名称+逗号对齐+日期）")
    print("  3. 会议历届时间.csv — 会议名称、时间（便于后续处理）")
    print(f"  会议名单 {len(list_rows)-1} 条；历届时间 {len(LIJIE_LINES)} 条。")

if __name__ == "__main__":
    main()
