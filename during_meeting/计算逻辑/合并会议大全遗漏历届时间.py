def _parse_start_date(time_str):
    """从「YYYY-MM-DD 至 YYYY-MM-DD」解析起始日，用于排序。"""
    s = (time_str or "").strip()
    if " 至 " in s:
        return s.split(" 至 ")[0].strip()[:10]
    return s[:10] if len(s) >= 10 else ""


