def run():
    if not PATH_MEETING_TIMES.exists():
        print(f"未找到 {PATH_MEETING_TIMES}")
        return

    # 读取现有历届时间表
    existing = []
    seen_names = set()
    with open(PATH_MEETING_TIMES, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames or ["会议名称", "时间"]
        for row in reader:
            name = (row.get("会议名称") or "").strip()
            time_str = (row.get("时间") or "").strip()
            if name:
                existing.append({"会议名称": name, "时间": time_str})
                seen_names.add(name)

    # 追加遗漏的届次（不重复 会议名称）
    added = 0
    for name, time_str in EXTRA_SESSIONS:
        if name in seen_names:
            continue
        existing.append({"会议名称": name, "时间": time_str})
        seen_names.add(name)
        added += 1

    # 按时间起始日排序
    existing.sort(key=lambda r: _parse_start_date(r.get("时间") or ""))

    with open(PATH_MEETING_TIMES, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(existing)

    print(f"已合并写入 {PATH_MEETING_TIMES}，共 {len(existing)} 行；本次新增 {added} 条（会议大全遗漏的历届时间）。")


if __name__ == "__main__":
    run()
