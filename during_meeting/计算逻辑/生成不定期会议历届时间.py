# -*- coding: utf-8 -*-
"""
生成「不定期会议历届时间」：仅含会议名称、开始日期、结束日期，便于查会议窗口做股票数据。
含：全国金融工作会议、全国科技创新大会、中央城镇化工作会议、一带一路国际合作高峰论坛。
日期已按公开报道核对（如 2002 届金融工作会 2月5-7日、2017 届 7月14-15日、2012 年科技创新大会 7月6-7日）。
"""
import csv
from pathlib import Path

# 不定期会议历届时间（公开资料核对）
BUQI_DATA = [
    ("全国金融工作会议（1997）", "1997-11-17", "1997-11-19"),
    ("全国金融工作会议（2002）", "2002-02-05", "2002-02-07"),
    ("全国金融工作会议（2007）", "2007-01-19", "2007-01-20"),
    ("全国金融工作会议（2012）", "2012-01-06", "2012-01-07"),
    ("全国金融工作会议（2017）", "2017-07-14", "2017-07-15"),
    ("中央金融工作会议（2023）", "2023-10-30", "2023-10-31"),
    ("全国科技创新大会（2006）", "2006-01-09", "2006-01-09"),
    ("全国科技创新大会（2012）", "2012-07-06", "2012-07-07"),
    ("全国科技创新大会（2016）", "2016-05-30", "2016-05-30"),
    ("全国科技创新大会（2022）", "2022-06-06", "2022-06-06"),
    ("全国科技创新大会（2023）", "2023-09-06", "2023-09-06"),
    ("中央城镇化工作会议（2013）", "2013-12-12", "2013-12-13"),
    ("中央城镇化工作会议（2023）", "2023-12-15", "2023-12-16"),
    ("一带一路国际合作高峰论坛（2017）", "2017-05-14", "2017-05-15"),
    ("一带一路国际合作高峰论坛（2019）", "2019-04-25", "2019-04-27"),
    ("一带一路国际合作高峰论坛（2023）", "2023-10-17", "2023-10-18"),
]

def main():
    base = Path(__file__).resolve().parent.parent
    dir_time = base / "时间"

    # CSV：会议名称, 开始日期, 结束日期, 时间（开始 至 结束）
    with open(dir_time / "不定期会议历届时间.csv", "w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["会议名称", "开始日期", "结束日期", "时间"])
        for name, start, end in BUQI_DATA:
            w.writerow([name, start, end, f"{start} 至 {end}"])

    # TXT：与会议历届时间相同的图格式（名称+逗号对齐+开始 至 结束）
    TARGET_LEN = 42
    lines = []
    for name, start, end in BUQI_DATA:
        date_str = f"{start} 至 {end}"
        n_commas = max(0, TARGET_LEN - len(name))
        lines.append(name + "," * n_commas + date_str)
    with open(dir_time / "不定期会议历届时间.txt", "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"已生成 时间/不定期会议历届时间.csv 与 不定期会议历届时间.txt，共 {len(BUQI_DATA)} 条。")
    print("用于查会议窗口的股票数据时，可直接用「开始日期」「结束日期」做区间。")

if __name__ == "__main__":
    main()
