#!/usr/bin/env bash
# -*- coding: utf-8 -*-
# 一键生成「相对开盘价」明细并更新「受影响显著的一级行业_最终版」中的开盘口径三列
# 顺序：相对开盘价行业数据 → 按窗口拆分 → 生成各时间轴行业涨跌明细(相对开盘价) → 更新最终版表
#
# 用法：可从任意目录执行（脚本会定位到自身所在目录再跑 .py），例如：
#   ./计算逻辑/生成相对开盘价并更新最终版.sh
# 或在项目根目录：  bash 计算逻辑/生成相对开盘价并更新最终版.sh

set -e
# 使用脚本所在目录的绝对路径，无论从哪执行都能找到同目录下的 .py 文件
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$SCRIPT_DIR"

echo "========== 1/4 会议窗口行业数据（相对开盘价） =========="
echo "说明：需已配置 rqdatac（米筐），输出到 时间/会议窗口行业数据_相对开盘价.csv"
python3 "$SCRIPT_DIR/会议窗口行业数据_米筐_相对开盘价.py"

echo ""
echo "========== 2/4 按窗口拆分（相对开盘价） =========="
python3 "$SCRIPT_DIR/split_会议窗口按窗口.py" 相对开盘价

echo ""
echo "========== 3/4 生成各会议各时间轴一级行业涨跌明细（相对开盘价） =========="
python3 "$SCRIPT_DIR/生成各会议各时间轴行业涨跌明细.py" 相对开盘价

echo ""
echo "========== 4/4 更新最终版表（补全开盘口径三列） =========="
python3 "$SCRIPT_DIR/受影响显著行业_涨跌幅统计.py"

echo ""
echo "完成。请查看 会议窗口数据/受影响显著的一级行业_最终版.csv 中「平均涨跌幅_开盘」等三列是否已填满。"
